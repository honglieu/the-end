import { NgStyle } from '@angular/common';
import { ChangeDetectionStrategy, Component, Input } from '@angular/core';
import { AngularSvgIconModule, SvgIconRegistryService } from 'angular-svg-icon';
import { ETrudiIconType } from '../../views/trudi-icon/trudi-icon.enum';

@Component({
  standalone: true,
  selector: 'trudi-icon',
  templateUrl: './trudi-icon.component.html',
  styleUrls: ['./trudi-icon.component.scss'],
  changeDetection: ChangeDetectionStrategy.OnPush,
  imports: [AngularSvgIconModule]
})
export class TrudiIconComponent {
  @Input() icon: IconType;
  @Input() style: NgStyle;
  @Input() viewBox: string;
  @Input() stretch: boolean;
  @Input() svgClass: string;
  @Input() svgAriaLabel: string;

  public iconRef: string;
  constructor(private iconReg: SvgIconRegistryService) {}

  ngOnChanges(): void {
    if (IconType[this.icon] || ETrudiIconType[this.icon]) {
      this.iconRef = IconType[this.icon] || ETrudiIconType[this.icon];
      if (IconType[this.icon]) {
        this.iconReg
          .loadSvg(`/assets/icon/${this.iconRef}.svg`, this.iconRef)
          .subscribe();
      } else {
        this.iconReg
          .loadSvg(`/assets/icon/trudi-icon/${this.iconRef}.svg`, this.iconRef)
          .subscribe();
      }
    }
  }
}

